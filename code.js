function isNumeric(input) {
    if(isNaN(input)) {
        return false;
    } else {
        return true;
    }
}

function sayHello(input) {
    if (input === "Alex") {
        return "Hello, Alex!";
    } else if (input === "Pat") {
        return "Hello, Pat!";
    } else {
        return "Hello, Jane!";
    }
}



